#include<iostream>
using namespace std;

int main() {
    char s[] = "welcome";  // String initialization
    int i;

    // Loop to find the length of the string
    for (i = 0; s[i] != '\0'; i++) {
        // The loop will terminate when s[i] becomes '\0' (null character)
    }

    cout << "Length of the string: " << i << endl;  // Print the length
    return 0;
}
